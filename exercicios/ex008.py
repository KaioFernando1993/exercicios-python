medida = float(input('digite uma medida : '))
decimetro = medida * -10
centimetro = medida * -100
milimitro = medida * -1000
kilometro = medida * 1000
hectometro = medida * 100
decametro = medida * 10

print('A medida de {}m corresponde a\n {}dc\n {}cm\n {}mm\n {}km\n {}he\n {}dec'.format(medida, decimetro, centimetro, milimitro, kilometro, hectometro, decametro ))
      
